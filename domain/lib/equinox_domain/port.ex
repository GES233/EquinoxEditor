defmodule EquinoxDomain.Port do
  @moduledoc """
  负责领域模型与外部引擎之间的数据协商。

  ## 职责范围

  它不直接描述渲染算法。

  其主要描述：

  * 引擎需要什么数据
  * 外部模型生成的哪些数据被允许作为 Artifect
    被保存到领域下的模块（e.g. UI 所需）
  * 用户如何介入模型结果
  * 模型结果如何与领域事实合并
  * 哪些结果允许被采纳为新的领域事实

  Port 本身不关心具体引擎实现，
  只定义协商协议与生命周期。

  同时，Port 不要求领域模型持有最终结果。

  最终输入由源于用户的领域模型、引擎的上游节点的计算结果、
  用户的额外干涉共同解析得到。

  ## 渲染流程

  ### 声明协议

  ### 渲染请求

  Projection 与用户事实经过 resolver 后，
  生成一次渲染所需的临时输入视图（RenderRequest）。

  RenderRequest 不属于持久化领域状态，
  而是一次编译/渲染快照。

  ### 采纳修改

  模型输出默认属于 Artifact，不会自动成为领域事实。

  用户可以显式采纳 Projection 或 Resolution 的部分结果，
  使其转化为新的领域数据。

  采纳行为一般是部分的、可冲突解决的，类似版本控制系统中的 merge workflow。
  
