defmodule EquinoxDomain.Timeline do
  # 时间系统是地基，很重要

  # ┌─────────────────────────────────────────────────┐
  # │  Musical Time（音乐时间）                       │
  # │  "第2小节 第3拍 第240刻"                        │
  # │  单位：Bar:Beat:Tick                            │
  # │  特点：与 tempo 无关，纯粹描述音乐结构          │
  # ├─────────────────────────────────────────────────┤
  # │  Tick（绝对刻）                                 │
  # │  从乐曲开头起算的累计 tick 数                   │
  # │  单位：non_neg_integer                          │
  # │  特点：这是内部存储的主键                       │
  # ├─────────────────────────────────────────────────┤
  # │  Physical Time（物理时间）                      │
  # │  单位：秒 / 采样点                              │
  # │  特点：送给音频引擎的最终坐标                   │
  # └─────────────────────────────────────────────────┘

  @type physical_time :: float()
end
